//Asssignment Operator -> = += -= *= /= 

public class AssignmentOperator {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a += 10);
        System.out.println(a -= 10);
        System.out.println(a *= 10);
        System.out.println(a /= 10);
        System.out.println(2 * (5 / 2));
    }
}
