public class Output{
    public static void main(String[] args) {
        System.out.println("Hello World");//the output after this will be printed in next line
        System.out.print("Hello World");//the output after this will be printed in same line
        System.out.print("Hello World\n");//"\n" also prints the output after this in next line
        System.out.print("Hello World\n");

        //to print pattern
        /* 
            *****
            ***
            **
            *       
        */
        System.out.println("****");
        System.out.println("***");
        System.out.println("**");
        System.out.println("*");
    }
}