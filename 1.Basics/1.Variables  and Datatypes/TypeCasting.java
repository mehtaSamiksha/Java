import java.util.*;

public class TypeCasting {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        float a = 25.50f;
        int b = (int) a;
        System.out.println(b);

        char ch = 'a';
        int num = ch;
        System.out.println(num);

    }
}
